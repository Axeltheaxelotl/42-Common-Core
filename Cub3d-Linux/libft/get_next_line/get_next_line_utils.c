#include "get_next_line.h"

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		++i;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

void	free_filedata(t_filedata *filedata)
{
	if (filedata->content != NULL)
	{
		free(filedata->content);
		filedata->content = NULL;
		filedata->position = 0;
	}
}
