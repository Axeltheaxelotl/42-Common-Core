#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*array;
	int		o_check;

	o_check = nmemb * size;
	if (nmemb != 0 && o_check / nmemb != size)
		return (NULL);
	array = (void *)malloc(nmemb * size);
	if (array == NULL)
		return (NULL);
	ft_bzero(array, (nmemb * size));
	return (array);
}
